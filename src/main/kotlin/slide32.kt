fun main() {
    val myText = "Anthony" // Define uma variável chamada myText e atribui o valor "Anthony" a ela
    println(myText) // Imprime o conteúdo da variável myText, que é "Anthony"
    val newText = myText.replace("An", "En") // Cria uma nova variável chamada newText, que contém a string myText com todas as ocorrências de "An" substituídas por "En"
    println(newText) // Imprime o conteúdo da variável newText, que será "Enthony" após a substituição
}
