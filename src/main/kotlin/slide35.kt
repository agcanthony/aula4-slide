fun main() {
    val nota = 5 // Define uma variável chamada nota e atribui o valor 5 a ela
    var resultado: String // Declara uma variável chamada resultado como uma String
    if (nota >= 7) { // Verifica se a nota é maior ou igual a 7
        resultado = "Aprovado" // Se a nota for maior ou igual a 7, atribui "Aprovado" à variável resultado
    }
    else if (nota < 5) { // Se a nota não for maior ou igual a 7, verifica se é menor que 5
        resultado = "Reprovado" // Se a nota for menor que 5, atribui "Reprovado" à variável resultado
    }
    else {
        resultado = "Recuperação" // Se a nota não for nenhuma das anteriores, atribui "Recuperação" à variável resultado
    }

    println(resultado) // Imprime o valor da variável resultado, que depende do valor da nota
}

